    function toggleMenu() {
        document.getElementsByClassName("custom-menu-button-1")[0].classList.toggle("cm_active-1")
        document.getElementsByClassName("custom-menu-button-2")[0].classList.toggle("cm_active-2")
        document.getElementsByClassName("custom-menu-button-3")[0].classList.toggle("cm_active-3")
        document.getElementById("side-menu").classList.toggle("display-none")
        document.getElementsByClassName("custom-menu-button")[0].classList.toggle("position-fixed")

    }


    var acc = document.getElementsByClassName("side-menu-item");
    var i;

    for (i = 0; i < acc.length; i++) {
        console.log(acc[i]);
        acc[i].addEventListener("click", function() {
            this.classList.toggle("active");
            var panel = this.nextElementSibling;
            if (panel.style.display === "block") {
                panel.style.display = "none";
            } else {
                panel.style.display = "block";
            }
        });
    }
</script>